

# if-satser - datorn fattar beslut

alder = 38

# Om (if) något stämmer - gör detta

if alder >= 18:
    print("Du är vuxen!")
else:
    print("Du är minderårig!")

# elif - flera val

poang = 75

if poang >= 90:
    print("Betyg: A - Utmärkt!")
elif poang >= 80:
    print("Betyg: B - Mycket bra!")
elif poang >= 70:
    print("Betyg: C - Bra!")
elif poang >= 60:
    print("Betyg: D Godkänt!")
elif poang >= 50:
    print("Betyg: F Underkänt!")


# Jämförelseoperatorer

x = 10
y = 20

print(x == y)      # Är x lika med y?         False
print(x != y)      # Är x inte lika med y?    True
print(x > y)       # Är x större än y?        False
print(x < y)       # Är x mindre än y?        True   
print(x >= y)      # Är x större/lika med?    False
print(x <= y)      # Är x mindre/lika med?    True




